public class Java_Operators {
    public static void main(String[] args) {
        int a = 50, b = 20;

        int sum = a + b;
        int minus = a - b;
        int product = a * b;
        double division = a / b;

        System.out.println("Sum = "+sum);
        System.out.println("Minus = "+minus);
        System.out.println("Product = "+product);
        System.out.println("Division = "+division);
    }
}
